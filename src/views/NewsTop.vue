                            <p class="f_center">
                                <img alt="埃梅里力挺厄齐尔 将帮助厄齐尔和俱乐部更得更好" src="http://cms-bucket.nosdn.127.net/2018/07/15/b8e0f8feefe34ad3902d2851925410ad.png?imageView&amp;thumbnail=550x0">
