                            <p></p>
                            <div class="gg200x300">
                                <img src="../assets/img/newstop/gg_0.jpg" alt="">
                            </div>
                            <p>埃梅里说：“厄齐尔对我们来说是一位非常重要的球员，我们希望能帮助到他，让他和我们相处的感觉是愉快舒适的。虽然德国队没能取得理想的成绩，但他现在最重要的先休息一阵子，好好度假。他现在应该放松，到时候以最好的转态回归俱乐部。我想帮助他个人以及整个俱乐部都变得更好。过去的一年对整个球队都很困难，不仅仅是厄齐尔个人，联赛第6名并不是大家想要的成绩。”</p>
                            <p>对于俱乐部，埃梅里表示希望球员们早日归来，但状态很重要，因此会让他们得到充分的休息时间，他说：“我个人当然很希望各位国脚们都尽快归队进行训练，但是他们的状态也非常重要，因为新赛季非常非常困难和非常长，所以他们现在就需要好好休息，恢复好体能，为下个赛季做好准备。”</p>
